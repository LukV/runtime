#!/usr/bin/env node
// One-shot generator for the runtime wordmark SVG.
//
// Outlines `runtime` from Source Serif 4 Variable Roman at weight 500
// (Medium), applies -0.02em tracking per 004 §1, and emits
// `dist/wordmark.svg` with the amber period as a separate <circle> so the
// brand device never depends on font loading (per 004 §8 #5 resolution).
//
// Run rarely — the SVG is committed and reproduction is only needed if
// the wordmark itself changes. Requires the Source Serif 4 Variable
// Roman TTF to be available locally:
//
//   curl -L -o /tmp/SourceSerif4Variable-Roman.ttf \
//     https://raw.githubusercontent.com/adobe-fonts/source-serif/release/VAR/SourceSerif4Variable-Roman.ttf
//   FONT_PATH=/tmp/SourceSerif4Variable-Roman.ttf \
//     node packages/design-tokens/scripts/generate-wordmark.mjs
//   rm /tmp/SourceSerif4Variable-Roman.ttf
//
// The font does not live in this repo. It's downloaded for the
// generation pass, then deleted.

import { readFileSync, writeFileSync, mkdirSync } from 'node:fs'
import { dirname, join } from 'node:path'
import { fileURLToPath } from 'node:url'
import opentype from 'opentype.js'

const HERE = dirname(fileURLToPath(import.meta.url))
const PKG = dirname(HERE)
const OUT_SVG = join(PKG, 'dist', 'wordmark.svg')
const OUT_TS = join(PKG, 'dist', 'wordmark.ts')

const FONT_PATH = process.env.FONT_PATH
if (!FONT_PATH) {
  console.error('FONT_PATH env var required. See script header for setup.')
  process.exit(1)
}

const WORDMARK_TEXT = 'runtime'
const FONT_SIZE = 100 // arbitrary; SVG scales via height in consumers
const WEIGHT = 500 // Medium per 004 §1
const TRACKING_EM = -0.02 // 004 §1 wordmark tracking

const buffer = readFileSync(FONT_PATH)
const font = opentype.parse(
  buffer.buffer.slice(buffer.byteOffset, buffer.byteOffset + buffer.byteLength),
)

// Set the variable axis to weight 500 (Medium).
if (font.variation && typeof font.variation.set === 'function') {
  font.variation.set({ wght: WEIGHT })
} else {
  console.warn('font has no variable axis; using whatever weight it ships with')
}

const trackingUnits = FONT_SIZE * TRACKING_EM
const scale = FONT_SIZE / font.unitsPerEm

// Render each glyph at its accumulated x, with tracking applied between letters.
let x = 0
const baseline = FONT_SIZE // arbitrary baseline y; geometry stays relative
const combined = new opentype.Path()

for (const char of WORDMARK_TEXT) {
  const glyph = font.charToGlyph(char)
  const glyphPath = glyph.getPath(x, baseline, FONT_SIZE)
  combined.extend(glyphPath)
  x += glyph.advanceWidth * scale + trackingUnits
}

// Period: extract the natural period's bounding box from Source Serif 4 at
// the same weight, then re-render it as a perfect circle in amber. The dot
// is placed after the last letter's advance, with the same tracking gap as
// between letters.
const periodGlyph = font.charToGlyph('.')
const periodPath = periodGlyph.getPath(x, baseline, FONT_SIZE)
const periodBB = periodPath.getBoundingBox()
const periodCx = (periodBB.x1 + periodBB.x2) / 2
const periodCy = (periodBB.y1 + periodBB.y2) / 2
const periodR = Math.max(periodBB.x2 - periodBB.x1, periodBB.y2 - periodBB.y1) / 2

// viewBox: bound the wordmark + period with a small padding margin so the
// dot never clips against the SVG edge at the right.
const PAD = 4
const wordmarkBB = combined.getBoundingBox()
const minX = Math.floor(Math.min(wordmarkBB.x1, periodBB.x1 - periodR) - PAD)
const minY = Math.floor(Math.min(wordmarkBB.y1, periodBB.y1) - PAD)
const maxX = Math.ceil(Math.max(wordmarkBB.x2, periodCx + periodR) + PAD)
const maxY = Math.ceil(Math.max(wordmarkBB.y2, periodBB.y2) + PAD)
const vbWidth = maxX - minX
const vbHeight = maxY - minY

const PRECISION = 2
const pathData = combined.toPathData(PRECISION)
const round = (n) => Math.round(n * 100) / 100

const svg = `<!-- AUTO-GENERATED by scripts/generate-wordmark.mjs — DO NOT EDIT.
     Source: Source Serif 4 Variable Roman @ wght=500, tracking -0.02em.
     Regenerate per the script header if the wordmark changes. -->
<svg xmlns="http://www.w3.org/2000/svg" viewBox="${minX} ${minY} ${vbWidth} ${vbHeight}" fill="currentColor" aria-hidden="true">
  <path d="${pathData}" />
  <circle cx="${round(periodCx)}" cy="${round(periodCy)}" r="${round(periodR)}" fill="var(--color-eerste-licht)" />
</svg>
`

mkdirSync(dirname(OUT_SVG), { recursive: true })
writeFileSync(OUT_SVG, svg)

const ts = `// AUTO-GENERATED FROM Source Serif 4 by scripts/generate-wordmark.mjs — DO NOT EDIT.
// The runtime wordmark as a portable data shape. Consumers (Wordmark.tsx,
// SwiftUI Wordmark) read these values and assemble the rendering.

export const WORDMARK_PATH = ${JSON.stringify(pathData)}

export const WORDMARK_PERIOD = {
  cx: ${round(periodCx)},
  cy: ${round(periodCy)},
  r: ${round(periodR)},
} as const

export const WORDMARK_VIEWBOX = {
  x: ${minX},
  y: ${minY},
  width: ${vbWidth},
  height: ${vbHeight},
} as const
`
writeFileSync(OUT_TS, ts)

console.log(`wrote ${OUT_SVG}`)
console.log(`wrote ${OUT_TS}`)
console.log(`  viewBox: ${minX} ${minY} ${vbWidth} ${vbHeight}`)
console.log(
  `  wordmark bbox: (${round(wordmarkBB.x1)}, ${round(wordmarkBB.y1)}) → (${round(wordmarkBB.x2)}, ${round(wordmarkBB.y2)})`,
)
console.log(`  period: cx=${round(periodCx)} cy=${round(periodCy)} r=${round(periodR)}`)
